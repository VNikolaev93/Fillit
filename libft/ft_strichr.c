#include "libft.h"

size_t	ft_strichr(const char *s, int c)
{
	size_t i;

	i = 0;
	while (i < (ft_strlen(s) + 1))
	{
		i++;
		if (s[i] == c)
			return (i);
	}
	return (0);
}
