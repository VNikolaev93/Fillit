#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	int j;

	j = 0;
	i = -1;
	while (s[j])
		j++;
	while (++i < (j + 1))
		if (s[i] == c)
			return ((char *)s + i);
	return (NULL);
}
